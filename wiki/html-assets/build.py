# ── Frontmatter ──────────────────────────────────────────────────
FRONTMATTER_RE = re.compile(r"\A[^\n]*?---\s*\n(.*?)\n---\s*\n", re.DOTALL)


@dataclass
class Frontmatter:
    title: str = ""
    tags: list[str] = field(default_factory=list)
    date: str = ""
    sources: list[str] = field(default_factory=list)
    related: list[str] = field(default_factory=list)


def parse_frontmatter(text: str) -> tuple[Frontmatter, str]:
    """Tolerant: accepts garbage chars before the first --- (e.g. kubernetes.md `k2---`)."""
    m = FRONTMATTER_RE.match(text)
    if not m:
        return Frontmatter(), text
    raw = m.group(1)
    body = text[m.end():]
    fm = Frontmatter()
    for line in raw.splitlines():
        if ":" not in line:
            continue
        k, _, v = line.partition(":")
        k, v = k.strip(), v.strip()
        if not v:
            continue
        if k == "title":
            fm.title = v.strip('"').strip("'")
        elif k == "date":
            fm.date = v.strip('"').strip("'")
        elif k in ("tags", "sources", "related"):
            items = re.findall(r"\[\[([^\]]+)\]\]|\"([^\"]+)\"|'([^']+)'|([^,\[\]\s]+)", v)
            flat = [a or b or c or d for (a, b, c, d) in items]
            flat = [x.strip().strip('"').strip("'").strip("[").strip("]") for x in flat if x]
            flat = [x for x in flat if x]
            setattr(fm, k, flat)
    return fm, body


# ── Wikilink resolution ──────────────────────────────────────────
class Resolver:
    """Index every wiki/*.md by stem → its category (entities/concepts/sources/analysis)."""

    def __init__(self) -> None:
        self.index: dict[str, str] = {}
        self.canonical: dict[str, str] = {}
        for cat in CATEGORIES:
            d = WIKI / cat
            if not d.is_dir():
                continue
            for f in d.glob("*.md"):
                self.index[f.stem] = cat
                self.index[f.stem.lower()] = cat
                self.canonical[f.stem] = f.stem
                self.canonical[f.stem.lower()] = f.stem

    def resolve(self, target: str) -> tuple[str, str] | None:
        """Return (category, canonical stem) for a target page; None if not found."""
        stem = target.strip()
        if "|" in stem:
            stem, _ = stem.split("|", 1)
        if "#" in stem:
            stem, _ = stem.split("#", 1)
        stem = stem.strip()
        if not stem:
            return None
        cat = self.index.get(stem) or self.index.get(stem.lower())
        if not cat:
            return None
        return cat, self.canonical.get(stem) or self.canonical.get(stem.lower()) or stem

    def href(self, target: str, *, from_cat: str | None) -> str | None:
        """Return relative href from a page in from_cat to target page; None if not found."""
        resolved = self.resolve(target)
        if not resolved:
            return None
        cat, real_stem = resolved
        target_path = f"{cat}/{real_stem}.html"
        if from_cat is None:
            return target_path  # called from wiki/html/index.html
        return f"../{target_path}"


# ── Wikilink preprocessing ───────────────────────────────────────
WIKILINK_RE = re.compile(r"\[\[([^\]]+)\]\]")


def rewrite_wikilinks(body: str, resolver: Resolver, from_cat: str | None) -> str:
    def repl(m: re.Match) -> str:
        target = m.group(1).strip()
        label = target
        if "|" in target:
            target, label = target.split("|", 1)
            target = target.strip()
            label = label.strip()
        href = resolver.href(target, from_cat=from_cat)
        if href:
            return f'<a class="wikilink" href="{href}">{htmllib.escape(label)}</a>'
        return f'<span class="wikilink wikilink-missing" title="未建页：{htmllib.escape(target)}">{htmllib.escape(label)}</span>'

    return WIKILINK_RE.sub(repl, body)


# ── TOC extraction ───────────────────────────────────────────────
HEADING_RE = re.compile(r"^(#{1,6})\s+(.+?)\s*$", re.MULTILINE)


def slugify(s: str) -> str:
    s = re.sub(r"[<>&\"']+", "", s)
    s = re.sub(r"\s+", "-", s.strip().lower())
    s = re.sub(r"[^a-z0-9\u4e00-\u9fa5\-_]", "", s)
    return s[:80] or "section"


def extract_toc(body: str) -> list[tuple[int, str, str]]:
    """Return [(level, text, slug)] for h2/h3 (skip h1; it's the page title)."""
    toc = []
    seen: dict[str, int] = {}
    for m in HEADING_RE.finditer(body):
        hashes, text = m.group(1), m.group(2)
        level = len(hashes)
        if level < 2 or level > 3:
            continue
        slug = slugify(text)
        if slug in seen:
            seen[slug] += 1
            slug = f"{slug}-{seen[slug]}"
        else:
            seen[slug] = 1
        toc.append((level, text, slug))
    return toc


def inject_heading_ids(body: str) -> str:
    """Add {#slug} to h2/h3 so python-markdown's toc extension picks them up."""
    seen: dict[str, int] = {}

    def repl(m: re.Match) -> str:
        hashes, text = m.group(1), m.group(2)
        level = len(hashes)
        if level < 2 or level > 3:
            return m.group(0)
        # Strip wikilink syntax for slugification only
        plain = WIKILINK_RE.sub(r"\1", text)
        slug = slugify(plain)
        if slug in seen:
            seen[slug] += 1
            slug = f"{slug}-{seen[slug]}"
        else:
            seen[slug] = 1
        return f"{hashes} {text} {{#{slug}}}"

    return HEADING_RE.sub(repl, body)



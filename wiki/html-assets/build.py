# ── Driver ───────────────────────────────────────────────────────
def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("--force", action="store_true", help="Overwrite hand-crafted HTML too")
    ap.add_argument("--dry-run", action="store_true")
    args = ap.parse_args()

    resolver = Resolver()
    OUT.mkdir(parents=True, exist_ok=True)
    sync_html_assets(args.dry_run)
    index_body_html, descriptions = collect_index_descriptions(resolver)
    graph_data = collect_graph_data(resolver, descriptions)

    stats = {"written": 0, "skipped": 0, "total": 0}

    # Category pages
    for cat in CATEGORIES:
        src_dir = WIKI / cat
        if not src_dir.is_dir():
            continue
        dst_dir = OUT / cat
        dst_dir.mkdir(parents=True, exist_ok=True)
        for md_path in sorted(src_dir.glob("*.md")):
            page = load_page(md_path, cat)
            html = build_page(page, resolver, graph_data)
            dst = dst_dir / f"{md_path.stem}.html"
            stats["total"] += 1
            if not should_write(dst, args.force):
                normalize_stylesheet_href(dst, args.dry_run)
                stats["skipped"] += 1
                print(f"  skip (hand-crafted): {dst.relative_to(ROOT)}")
                continue
            if args.dry_run:
                print(f"  would write: {dst.relative_to(ROOT)}")
            else:
                dst.write_text(html, encoding="utf-8")
                stats["written"] += 1
                print(f"  ✓ {dst.relative_to(ROOT)}")

    # log.md → log.html
    log_md = WIKI / "log.md"
    if log_md.exists():
        page = load_page(log_md, "ROOT")
        html = build_page(page, resolver, graph_data)
        dst = OUT / "log.html"
        stats["total"] += 1
        if should_write(dst, args.force):
            if not args.dry_run:
                dst.write_text(html, encoding="utf-8")
            stats["written"] += 1
            print(f"  ✓ {dst.relative_to(ROOT)}")
        else:
            stats["skipped"] += 1

    # Site index
    dst = OUT / "index.html"
    stats["total"] += 1
    if should_write(dst, args.force):
        html = build_site_index(resolver, index_body_html, descriptions)
        if not args.dry_run:
            dst.write_text(html, encoding="utf-8")
        stats["written"] += 1
        print(f"  ✓ {dst.relative_to(ROOT)}")
    else:
        stats["skipped"] += 1

    # Knowledge graph
    graph_json_dst = OUT / "graph.json"
    if args.dry_run:
        print(f"  would write: {graph_json_dst.relative_to(ROOT)}")
    else:
        graph_json_dst.write_text(json.dumps(graph_data, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
        print(f"  ✓ {graph_json_dst.relative_to(ROOT)}")

    dst = OUT / "graph.html"
    stats["total"] += 1
    if should_write(dst, args.force):
        html = build_graph_page(graph_data)
        if args.dry_run:
            print(f"  would write: {dst.relative_to(ROOT)}")
        else:
            dst.write_text(html, encoding="utf-8")
            print(f"  ✓ {dst.relative_to(ROOT)}")
        stats["written"] += 1
    else:
        stats["skipped"] += 1

    print(
        f"\nDone. wrote {stats['written']} · skipped {stats['skipped']} (hand-crafted) · total {stats['total']}"
    )
    return 0


if __name__ == "__main__":
    sys.exit(main())
